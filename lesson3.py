#Напишите программу, которая найдёт произведение пар чисел списка.
# Парой считаем первый и последний элемент, второй и предпоследний и т.д.
list_b = [2, 3, 4, 5, 6]
l = len(list_b)//2
l = l if len(list_b) % 2 == 0 else l + 1
list_c = []
for i in range (l):
    list_c.append(list_b[i] * list_b[-i-1])
print(f' для списка {list_b} получившийся список произведений пар =  {list_c}')

