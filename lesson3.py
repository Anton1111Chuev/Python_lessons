#Задайте число. Составьте список чисел Фибоначчи, в том числе для отрицательных индексов.
#F−1 = 1,
#F−2 = -1,
#Fn = F(n+2)−F(n+1).
k = 8;
list_fib = [-1,-1, 1, 1]
pos = 4
for i in range(4, k + 2):
    el = (list_fib[pos-1] + list_fib[pos-2])
    list_fib.insert(0, -el)
    list_fib.append(el)
    pos +=2
print(f'Числа Негафибоначчи для {k} = {list_fib}')